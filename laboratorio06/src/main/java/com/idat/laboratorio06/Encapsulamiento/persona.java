package com.idat.laboratorio06.Encapsulamiento;

/**
 *
 * @author IDAT
 */
public class persona {
    private String name;

    public persona(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "persona{" + "name=" + name + '}';
    }
    
}
